class TestCompanyDataUpdater(TransactionTestCase):
    DATA_DIR = os.path.join(os.path.dirname(__file__), "./data")

    def setUpData(self, document_types, count=-1):
        if not os.path.exists(self.DATA_DIR):
            os.mkdir(self.DATA_DIR)
        document_list = json.loads(LISTS.strip())
        documents = edinet.models.Documents.create(document_list)

        targets = [d for d in documents.list
                   if d.doc_type_code in document_types
                   and d.ordinance_code == "010"]
        if count > 0:
            targets = targets[:count]

        class StorageMock():

            def download_file(self, source, target):
                shutil.copyfile(source, target)

        register = EDINETDocumentRegister()
        extractor = EDINETFeatureExtractor(StorageMock())

        registered = []
        for t in targets:
            xbrl = t.document_id + "_1.xbrl"
            pdf = t.document_id + "_2.pdf"
            xbrl_path = os.path.join(self.DATA_DIR, xbrl)
            pdf_path = os.path.join(self.DATA_DIR, pdf)
            print(t.document_id)
            if not os.path.exists(xbrl_path):
                if t.has_xbrl:
                    t.get_xbrl(save_dir=self.DATA_DIR)
                else:
                    xbrl_path = ""

            if not os.path.exists(pdf_path):
                if t.has_pdf:
                    t.get_pdf(save_dir=self.DATA_DIR)
                else:
                    pdf_path = ""

            r = register.register_document(t, xbrl_path, pdf_path)
            extractor.extract_feature(r, "executive_state.number_of_executives")

        return targets, registered

    def test_update_company_data(self):
        targets, registered = self.setUpData(("120",))
        date = datetime(2018, 3, 1)
        assert len(targets) > 0

        service = CompanyDataUpdater()
        companies = EDINETCompany.objects.all()
        feature_name = "executive_state.number_of_executives"

        for c in companies:
            updates = service.update_company_data(
                        c, date, feature_name, dryrun=True)

            self.assertIn(feature_name, updates)
            if c.edinet_code == "E33416":
                # periodStart: 2017-12-21 ~ periodEnd: 2018-06-20
                # date: 2018-03-01 then target ym is
                # len(2018/3, 2018/4, 2018/5, 2018/6) = 4
                self.assertEqual(len(updates[feature_name]), 4)
            else:
                # periodStart: 2018-01-01 ~ periodEnd: 2018-06-30
                # date: 2018-03-01 then target ym is
                # len(2018/3, 2018/4, 2018/5, 2018/6) = 4
                self.assertEqual(len(updates[feature_name]), 4)

            _date = datetime(date.year, date.month, date.day)
            for i, k in enumerate(updates[feature_name]):
                self.assertEqual(k.year, _date.year)
                self.assertEqual(k.month, _date.month)
                self.assertGreater(
                    updates[feature_name][k].number_of_executives.value, 0)

                n_year, n_month = calendar.nextmonth(_date.year, _date.month)
                _date = datetime(n_year, n_month, date.day)


